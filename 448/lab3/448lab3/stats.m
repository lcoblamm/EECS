vars(2) = var(double(greenv));
vars(3) = var(double(bluev));
vars(4) = var(double(rgbv));
vars(5) = var(double(lumv));
mins(1) = min(redv);
mins(2) = min(greenv);
mins(3) = min(bluev);
mins(4) = min(rgbv);
mins(5) = min(lumv);
maxs(1) = max(redv);
maxs(2) = max(greenv);
maxs(3) = max(bluev);
maxs(4) = max(rgbv);
maxs(5) = max(lumv);
